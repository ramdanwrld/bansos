        <h2>Penjelasan Ditolaknya Pendaftar Sebagai Penerima Bansos Provinsi</h2><br>
        <p>I. Penjelasan Umum
        <p>Bantuan Sosial Provinsi (Bansos Provinsi) adalah program bantuan sosial yang diselenggarakan oleh Pemerintah Daerah Provinsi Jawa Barat. Bantuan ini diberikan kepada keluarga (bukan individu) yang mengalami kesulitan ekonomi akibat Pandemi Covid-19. Agar memastikan bantuan sosial tepat sasaran, Pemdaprov Jabar memberlakukan mekanisme penyaringan data pendaftar yang menyebabkan tidak semua pendaftar berhak mendapatkan bantuan sosial.

          <p>Pada aplikasi Sapawarga dan website Solidaritas, tercantum sekitar 27 alasan detail yang menjelaskan ditolaknya pendaftar sebagai penerima atau Keluarga Rumah Tangga Sasaran (KRTS) Bansos Provinsi. Untuk lebih singkatnya, berikut kami kategorikan penyebab ditolaknya usulan calon penerima bantuan ke dalam 5 kelompok alasan umum, yaitu:

            <p>A. Tidak Memenuhi Kriteria Administrasi 
              <p>B. Dianggap Tidak Layak Menerima Bantuan Sosial Provinsi Oleh Penilaian Pemerintah 
                <p>C. Sudah Terdaftar Sebagai Penerima Manfaat Pada Pintu Bantuan lain 
                  <p>D. Terjadi Kesalahan Pada Proses Input Data 
                    <p>E. Berpotensi Atau Telah Mengalami Gagal Salur
                      <br><br>
                      <p>II. Daftar Istilah
                        <p>Beberapa istilah yang perlu dipahami masyarakat untuk memahami proses penyelenggaraan bansos, antara lain: 

                          <p>1.KRTS: Keluarga Rumah Tangga Sasaran 
                            <p>2.DTKS: Data Kesejahteraan Sosial Terpadu 
                              <p>3.Non-DTKS: Data yang tidak tercantum dalam sistem DTKS (Penerima Bansos Provinsi, Bansos 4.Kab/Kota, dan BLT Dana Desa) 
                                <p>5.Nomor KK: Nomor Kartu Keluarga 
                                  <p>6.NIK: Nomor Induk Kependudukan yang tercantum pada KTP 
                                    <p>7.Kab/Kota: Pemerintah Daerah Kabupaten/Kota 
                                      <p>8.BPKP: Badan Pengawas Keuangan dan Pembangunan
                                        <br><br>
                                        <p>III. Penjelasan Detail
                                          <p>Berikut adalah penjelasan detail mengenai tidak terdaftar/ditolak sebagai penerima manfaat (KRTS)
                                            <p>A. Tidak Memenuhi Kriteria Administrasi
                                              <p>1.Tidak Cukup Umur: Nama penerima pada bansos Non-DTKS, wajib merupakan representasi dari Keluarga atau seorang Kepala Keluarga. Calon penerima dengan usia di bawah 17 tahun akan dihapus dari daftar. 
                                                <p>2.KK Duplikat: 1 Nomor KK untuk satu bantuan. Apabila terdapat keluarga dengan NIK berbeda namun memiliki nomor KK serupa, maka salah satu nama penerima akan dihapus. 
                                                  <p>3.Pekerjaan Tidak Sesuai: Kepala keluarga berasal dari profesi yang disebutkan dianggap tidak layak untuk menerima bantuan sosial provinsi. Profesi tersebut antara lain adalah anggota BPK, anggota DPR RI, anggota DPRD Kabupaten, anggota DPRD Provinsi, anggota Mahkamah Konstitusi, anggota kepolisian RI (POLRI), anggota Pegawai Negeri Sipil (PNS), dan anggota Tentara Nasional Indonesia (TNI). 
                                                    <p>4.NIK DTKS: NIK pendaftar telah tercantum pada daftar DTKS. 
                                                      <p>5.No KK DTKS: Nomor KK pendaftar telah tercantum pada daftar DTKS. 
                                                        <p>6.Nama berbeda dengan Dukcapil: NIK yang diusulkan pendaftar berbeda dengan database kependudukan Kemendagri. 
                                                          <p>7.Kombinasi Nama Alamat: Nama dan alamat yang diusulkan pendaftar berbeda dengan database kependudukan Kemendagri.
                                                            <br><br>
                                                            <p>B. Dianggap Tidak Layak Menerima Bantuan Sosial Provinsi Oleh Penilaian Pemerintah
                                                              <p>1.Dikeluarkan Kab/Kota: Pemerintah Kabupaten/Kota menilai pendaftar yang bersangkutan tidak layak menerima Bansos Provinsi. 
                                                                <p>2.Ditolak Sapawarga: Pemerintah Desa atau RW menilai pendaftar yang bersangkutan tidak layak menerima Bansos Provinsi. 
                                                                  <p>3.Cleansing BPKP: BPKP menganggap penerima yang bersangkutan tidak layak menerima Bansos Provinsi.
                                                                    <br><br>
                                                                    <p>C. Sudah Terdaftar Sebagai Penerima Manfaat Pada Pintu Bantuan Lain
                                                                      <p>Penyebab pendaftar ditolak sebagai calon penerima manfaat Bansos Provinsi adalah karena telah terdaftar sebagai :
                                                                        <p>1.Penerima Program Keluarga Harapan 
                                                                          <p>2.Penerima Bantuan Pangan Non Tunai 
                                                                            <p>3.Penerima Bantuan Pangan Non Tunai Perluasan 
                                                                              <p>4.Penerima Bantuan Langsung Tunai Kemensos 
                                                                                <p>5.Penerima Bantuan Presiden 
                                                                                  <p>6.Penerima Bantuan Provinsi DTKS 
                                                                                    <p>7.Penerima Bantuan Kab/Kota 
                                                                                      <p>8.Penerima Dana Desa
                                                                                        <br><br>
                                                                                        <p>D. Terjadi Kesalahan Pada Proses Input Data
                                                                                          <p>1.Kode Wilayah Tidak Ditemukan: Terjadi kesalahan pada saat pendaftar melakukan input data, salah satunya saat menggunakan fitur import data atau karena faktor lain, sehingga kode wilayah terhapus atau salah, dan menyebabkan data sulit diidentifikasi validitasnya. 
                                                                                            <p>2.Format Nama Salah: Nama yang didaftarkan ke dalam sistem tidak tampak seperti nama yang benar (contoh: hanya satu huruf atau berisi kombinasi huruf yang tidak terbaca sebagai nama)
                                                                                              <br><br>
                                                                                              <p>E. Berpotensi Atau Telah Mengalami Gagal Salur
                                                                                                <p>1.Alamat Tidak Lengkap: Penulisan alamat dianggap tidak memenuhi standar kelengkapan, sehingga tidak layak kirim menurut standar PT Pos Indonesia 
                                                                                                  <p>2.Aduan Pindah dan Meninggal: Terdapat masyarakat yang melaporkan bahwa pendaftar yang bersangkutan telah pindah atau meninggal sehingga tidak dapat menerima bantuan sosial. 
                                                                                                    <p>3.Gagal Salur PT POS: Paket yang dikirimkan kepada penerima bantuan sosial mengalami gagal salur pada distribusi bantuan sosial tahap sebelumnya, sehingga dihapus dari daftar penerima bantuan sosial pada tahap selanjutnya.
                                                                                                    </p>


